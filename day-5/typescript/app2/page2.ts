// implicitly (type inferenced)

// number
let num = 100
console.log(`num = ${num}, type = ${typeof(num)}`)

// not possible to set string value to number
// num = 'test'

// number 
const salary = 5.6
console.log(`salary = ${salary}, type = ${typeof(salary)}`)

// string 
const firstName = 'steve'
console.log(`firstName = ${firstName}, type = ${typeof(firstName)}`)

// boolean
const canVote = true
console.log(`canVote = ${canVote}, type = ${typeof(canVote)}`)

// object
const person = { name: 'person1', address: 'Pune' }
console.log(`person = ${person}, type = ${typeof(person)}`)

// undefined
let myVar
console.log(`myVar = ${undefined}, type = ${typeof(undefined)}`)
